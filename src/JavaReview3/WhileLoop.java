package JavaReview3;

public class WhileLoop {
    public static void main(String[] args) {

      int time=9;

      if (time>7){
          System.out.println("Hi");
      }
// "While" makes it an infinite loop till condition is false
      while(time>7){
          System.out.println("Hi");
          time--;
      }
        System.out.println("Value of time= "+time);










    }
}
