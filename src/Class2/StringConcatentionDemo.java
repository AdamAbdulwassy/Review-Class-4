package Class2;

public class StringConcatentionDemo {
    public static void main(String[] args) {
        String name="john";
        int age=27;
        System.out.println(name);
        System.out.println(age);
        System.out.println(name+age);
        System.out.println(name+" "+age);// <-- this was adds spaces to output

    }
}
