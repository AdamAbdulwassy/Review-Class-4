package HW;

public class C7oneTo100Flipped {
    public static void main(String[] args) {

        int num=100;
        while(num>=1){
            System.out.print(num+" ");
            num-=1;
        }
        System.out.println();
        for (int a = 100; a >=1; a--) {
            System.out.print(a+" ");

        }
        System.out.println();
        int b=100;
        do{
            System.out.print(b+" ");
            b--;
        }while(b>=1);















    }
}
