public class C7oneTo100 {
    public static void main(String[] args) {

        int i=1;
        while(i<=100){
            System.out.print(i+" ");
            i++;
        }

        System.out.println();
        for (int a = 1; a <=100; a++) {

            System.out.print(a+" ");

        }
        System.out.println();
        int b=1;
        do{
            System.out.print(b+" ");
            b++;

        }while (b<=100);
















    }
}
